int	ft_str_is_alpha(char *str )
{
	int		i;
	char	*a;

	a = str;
	i = 0;
	if (a[0] == '\0')
	{
		return (1);
	}
	while (a[i] != '\0')
	{
		if (!((a[i] >= 'A' && a[i] <= 'Z') || (a[i] >= 'a' && a[i] <= 'z')))
			return (0);
		i++;
	}
	return (1);
}
